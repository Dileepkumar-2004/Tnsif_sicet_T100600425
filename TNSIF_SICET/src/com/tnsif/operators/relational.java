package com.tnsif.operators;

public class relational {
    public static void main(String[] args) {
        int a = 10, b = 20;
        System.out.println("a > b: " + (a > b));
        System.out.println("a < b: " + (a < b));
        System.out.println("a == b: " + (a == b));
        System.out.println("a != b: " + (a != b));
    }
}
